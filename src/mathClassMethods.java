
public class mathClassMethods {
	public static void main(String args[]) {
		System.out.println(Math.abs(-26.7)); //Gives the value of how much the number is away from zero
		System.out.println(Math.ceil(7.4)); //Rounds up the value to the nearest
		System.out.println(Math.floor(7.4)); //Rounds down the value to the nearest
		
		System.out.println(Math.max(5.2,8.6)); //Gives maximum of two numbers
		System.out.println(Math.min(5.2,8.6)); //Gives minimum of two numbers
		
		System.out.println(Math.pow(2,3)); //Gives power of a number
		System.out.println(Math.sqrt(9)); //Gives square root of a number
	}
}